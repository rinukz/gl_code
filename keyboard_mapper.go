package main

import (
	"strings"
)

// แผนที่แปลงแป้นพิมพ์ ไทย -> อังกฤษ (ครอบคลุมทั้งแป้นพิมพ์ปกติและแบบกด Shift)
var thaiToEngMap = map[rune]rune{
	// แถว QWERTY (ไม่กด Shift)
	'ๆ': 'q', 'ไ': 'w', 'ำ': 'e', 'พ': 'r', 'ะ': 't', 'ั': 'y', 'ี': 'u', 'ร': 'i', 'น': 'o', 'ย': 'p', 'บ': '[', 'ล': ']', 'ฃ': '\\',
	// แถว QWERTY (กด Shift)
	'๐': 'Q', '"': 'W', 'ฎ': 'E', 'ฑ': 'R', 'ธ': 'T', 'ํ': 'Y', '๊': 'U', 'ณ': 'I', 'ฯ': 'O', 'ญ': 'P', 'ฐ': '{', ',': '}', 'ฅ': '|',
	// แถว ASDF (ไม่กด Shift)
	'ฟ': 'a', 'ห': 's', 'ก': 'd', 'ด': 'f', 'เ': 'g', '้': 'h', '่': 'j', 'า': 'k', 'ส': 'l', 'ว': ';', 'ง': '\'',
	// แถว ASDF (กด Shift)
	'ฤ': 'A', 'ฆ': 'S', 'ฏ': 'D', 'โ': 'F', 'ฌ': 'G', '็': 'H', '๋': 'J', 'ษ': 'K', 'ศ': 'L', 'ซ': ':', '.': '"',
	// แถว ZXCV (ไม่กด Shift)
	'ผ': 'z', 'ป': 'x', 'แ': 'c', 'อ': 'v', 'ิ': 'b', 'ื': 'n', 'ท': 'm', 'ม': ',', 'ใ': '.', 'ฝ': '/',
	// แถว ZXCV (กด Shift)
	'(': 'Z', ')': 'X', 'ฉ': 'C', 'ฮ': 'V', 'ฺ': 'B', '์': 'N', '?': 'M', 'ฒ': '<', 'ฬ': '>', 'ฦ': '?',
}

// forceEnglishVoucher แปลงข้อความไทยที่พิมพ์ผิด ให้เป็นภาษาอังกฤษ (และบังคับพิมพ์ใหญ่)
func forceEnglishVoucher(input string) string {
	var result strings.Builder
	for _, char := range input {
		// ถ้าตัวอักษรนั้นอยู่ใน Map (แปลว่าเป็นภาษาไทยที่พิมพ์ผิด)
		if engChar, exists := thaiToEngMap[char]; exists {
			result.WriteRune(engChar)
		} else {
			// ถ้าไม่ใช่ภาษาไทย (เป็นอังกฤษหรือตัวเลขอยู่แล้ว) ก็ใส่ลงไปเลย
			result.WriteRune(char)
		}
	}
	// บังคับให้ Voucher เป็นตัวพิมพ์ใหญ่ทั้งหมด (เพื่อความสวยงามและเป็นมาตรฐาน)
	return strings.ToUpper(result.String())
}
